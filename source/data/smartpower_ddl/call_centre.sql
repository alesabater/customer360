		TOTAL_CONSUMPTION DOUBLE NOT NULL,
		DATE DATE NOT NULL,
		TIME TIME NOT NULL,
		CONSUMPTION DOUBLE NOT NULL,
		DAY_POINT INT NOT NULL,
