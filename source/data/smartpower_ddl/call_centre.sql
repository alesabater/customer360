USE call_centre

CREATE TABLE csv_acd_calls(
		DEV_ID VARCHAR NOT NULL,
		DEV_TIMESTAMP LONG NOT NULL,
		TOTAL_CONSUMPTION DOUBLE NOT NULL,
		DATE DATE NOT NULL,
		TIME TIME NOT NULL,
		CONSUMPTION DOUBLE NOT NULL,
		DAY_POINT INT NOT NULL,
		MINUTE_POINT INT NOT NULL,
		MPRN VARCHAR NOT NULL UNIQUE,
	PRIMARY KEY(MPRN)
)

