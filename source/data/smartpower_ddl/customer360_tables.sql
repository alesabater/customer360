USE smart_power

DROP TABLE IF EXISTS customer_attributes;
CREATE TABLE customer_attributes(
        BUSINESS_PARTNER_NO INT NOT NULL,
        CONTRACT_AMOUNT INT NOT NULL,
        CREATION_DATE DATE NOT NULL,
        SWITCH INT NOT NULL,
        YEARS_SINCE_CREATION INT NOT NULL,
        AVG_BILLING_DATE DOUBLE,
        AVG_BILLING_YEARLY DOUBLE,
        AVG_KWH_DATE DOUBLE,
        AVG_KWH_YEARLT DOUBLE,
        CUSTOMER_DEBT DOUBLE,
	PRIMARY KEY(BILLING_NO)
);

DROP TABLE IF EXISTS customer_activities;
CREATE TABLE customer_activities(
		BUSINESS_PARTNER_NO INT NOT NULL,
        ACTIVITY INT NOT NULL,
		SUB_ACTIVITY INT NOT NULL,
        DATE DATE NOT NULL,
		YEAR INT,
		MONTH INT NOT NULL,
		DAY INT NOT NULL
	PRIMARY KEY(CONTRACT_NO)
);
