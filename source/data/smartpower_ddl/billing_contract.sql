USE billing_contract
CREATE TABLE table_billing(
		BUSINESS_PARTNER_NO VARCHAR NOT NULL UNIQUE,
		BILLING_NO INT NOT NULL UNIQUE,
		CONTRACT_ACCOUNT_NO VARCHAR NOT NULL,
		BILLING_DATE DATE NOT NULL,
		DOCUMENT_TYPE VARCHAR NOT NULL,
		BILLING_AMOUNT DOUBLE NOT NULL,
		CURRENCY_TYPE VARCHAR NOT NULL,
	PRIMARY KEY(BUSINESS_PARTNER_NO)
)
CREATE TABLE table_contract(
		BUSINESS_PARTNER_NO VARCHAR NOT NULL UNIQUE,
		MPRN LONG NOT NULL,
		CONTRACT_NO DOUBLE NOT NULL,
		CONTRACT_START_DATE DATE NOT NULL,
		CONTRACT_END_DATE DATE NOT NULL,
		CONTRACT_DESCRIPTION VARCHAR NOT NULL,
		MARKETING_ELEMENT VARCHAR NOT NULL,
		CONTRACT_STATUS_FLAG BOOLEAN NOT NULL,
		INTALMENT_NO INT NOT NULL,
		CONTRACT_EBILL_FLAGNOT BOOLEAN NOT NULL,
	PRIMARY KEY
)

CREATE table_discount(
		DISCOUNT_PACKAGE_ID INT NOT NULL UNIQUE,
		DISCOUNT_FROM_DATE DATE NOT NULL,
		DISCOUNT_TO_DATE DATE NOT NULL,
		DISCOUNT_PACKAGE_DESC VARCHAR NOT NULL,
		DISCOUNT_PACKAGE_PCT DOUBLE NOT NULL,
		INSTALMENT_NO DOUBLE NOT NULL,
		BUSINESS_PARTNER_NO INT NOT NULL,
	PRIMARY KEY(DISCOUNT_PACKAGE_ID)
)

CREATE TABLE table_payment(
		BILLING_NO INT NOT NULL UNIQUE,
		PAYMENT_NO INT NOT NULL UNIQUE,
		BUSINESS_PARTNER_NO VARCHAR NOT NULL,
		PAYMENT_DATE DATE NOT NULL,
		PAYMENT_AMOUNT DOUBLE NOT NULL,
		PAYMENT_TYPE VARCHAR NOT NULL,
	PRIMARY KEY(BILLING_NO)
)